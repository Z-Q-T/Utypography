function generateChineseLoremIpsum() {
	let LoremIpsum;
	LoremIpsum = "";

	ZiShu = document.getElementById('ZiShu').value;
	HouXuanZi = document.getElementById('HouXuanZi').value;

	if (ZiShu > 1000) {
		ZiShu = 1000;
	} else if (ZiShu < 1) {
		ZiShu = 1;
	}
	console.log("目标字数" + ZiShu);

	if (HouXuanZi > 5000) {
		HouXuanZi = 5000;
	} else if (HouXuanZi < 10) {
		HouXuanZi = 10;
	}
	console.log("候选字数" + HouXuanZi);

	maxFrequency = HanZi[HouXuanZi - 1][1];
	console.log("候选字最大频度" + maxFrequency);



	for (let i = 0; i < ZiShu; i++) {
		let HanZi_index = 0;
		let randomHanZi_number;
		let randomBiaoDian_number;
		let chosenHanZi;

		//生成汉字（一定会出一个字）
		function compareHanZiIndexWithHanZiFrequecy() {
			randomHanZi_number = Math.random() * maxFrequency;//汉字随机数最大不会超过最大候选字的频率
			for (HanZi_index = 0; HanZi_index < HanZi.length; HanZi_index++) {
				// console.log("汉字累计频度"+HanZi[j][1]);
				if (randomHanZi_number <= HanZi[HanZi_index][1]) {
					chosenHanZi = HanZi[HanZi_index][0];
					break;
				}
				// }
			}

			if (BiHui_choose2.checked) {
				for (k = 0; k < BiHuiZi.length; k++) {
					if (chosenHanZi == BiHuiZi[k]) {
						compareHanZiIndexWithHanZiFrequecy();
						break;
					}
				}
			} else if (BiHui_choose3.checked) {
				BiHuiZi_custom = document.getElementById('BiHui-input').value;

				for (k = 0; k < BiHuiZi_custom.length; k++) {
					if (chosenHanZi == BiHuiZi_custom[k]) {
						compareHanZiIndexWithHanZiFrequecy();
						break;
					}
				}
			}
		}
		compareHanZiIndexWithHanZiFrequecy();


		LoremIpsum = LoremIpsum + chosenHanZi;


		//生成标点（不一定会出标点）
		randomBiaoDian_number = Math.random() * 100;
		if (BiaoDian_choose2.checked) {
			for (k = 0; k < 2; k++) {
				if (randomBiaoDian_number <= BiaoDian[k][1]) {
					LoremIpsum = LoremIpsum + BiaoDian[k][0];
					break;
				}
			}
		} else if (BiaoDian_choose3.checked) {
			generatePunctuations(12);

		} else if (BiaoDian_choose4.checked) {
			generatePunctuations(BiaoDian.length);
		}


		// 循环到要补全闭标点的那个序号时，加上闭标点
		if (missingClosingPunctuation == true && i == closingPuncIndex) {
			finishPairPunctuations();
		}
		//如果所有字都生成好了，但还是缺闭标点，就直接加上闭标点
		if (missingClosingPunctuation == true && i == ZiShu - 1) {
			finishPairPunctuations();
		}

		//检查当前标点是不是成对标点（以后要不要补全闭标点）
		function detectPairPunctuations() {
			for (m = 0; m < ChengDuiBiaoDian.length; m++) {//遍历成对标点表里的每一个
				if (BiaoDian[k][0] == ChengDuiBiaoDian[m][0]) {
					missingClosingPunctuation = true;
					randomCharNumBetweenPairPuncs = Math.ceil(Math.random() * 10);//成对标点间有多少个汉字
					console.log("成对标点间字数" + randomCharNumBetweenPairPuncs);
					closingPuncIndex = i + randomCharNumBetweenPairPuncs;
					closingPunctuantion = ChengDuiBiaoDian[m][1];//待加上的闭标点
					console.log("待补标点" + closingPunctuantion);
					console.log("缺标点？" + missingClosingPunctuation);
					console.log("i=" + i);
					console.log("closingPuncIndex=" + closingPuncIndex);
				}
			}
		}

		//补全闭标点
		function finishPairPunctuations() {
			LoremIpsum = LoremIpsum + closingPunctuantion;
			missingClosingPunctuation = false;
			console.log("标点补全了");
		}

		//生成标点
		function generatePunctuations(availablePunctuations) {
			for (k = 0; k < availablePunctuations; k++) puncLoop: {

				if (i == closingPuncIndex) {
					break;
				}

				if (randomBiaoDian_number <= BiaoDian[k][1]) {//选一个标点出来
					if (missingClosingPunctuation == true) {
						for (m = 0; m < ChengDuiBiaoDian.length; m++) {
							if (BiaoDian[k][0] == ChengDuiBiaoDian[m][0]) {
								console.log("连续的成对标点，跳过");
								break puncLoop;
							}
						}
					}
					detectPairPunctuations();//这个标点是成对标点吗

					LoremIpsum = LoremIpsum + BiaoDian[k][0];
					break;
				}
			}
		}



	}
	//替换引号
	if (YingHao_choose2.checked) {
		LoremIpsum = LoremIpsum.replaceAll('“', '「');
		LoremIpsum = LoremIpsum.replaceAll('”', '」');
	} else if (YingHao_choose3.checked) {
		LoremIpsum = LoremIpsum.replaceAll('“', '『');
		LoremIpsum = LoremIpsum.replaceAll('”', '』');
	}

	document.getElementById('txt').value = LoremIpsum;
}



